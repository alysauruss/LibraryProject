    private void saveButtonHOMEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonHOMEActionPerformed

        String name = nameTextFieldHOME.getText().trim();
        String studnum = studNumTextFieldHOME.getText().trim();
        String program = programTextFieldHOME.getText().trim();
        String password = passfieldHOME.getText().trim();
        String yearlevel = (String) comboBox.getSelectedItem();

        
        boolean hasErrors = false;

        if (studnum.isEmpty()) {
            hasErrors = true;
            studNumTextFieldHOME.setBorder(BorderFactory.createLineBorder(Color.RED));
        }
        else {
            studNumTextFieldHOME.setBorder(UIManager.getBorder("TextField.border"));
        }
        ////
        if (name.isEmpty()) {
            hasErrors = true;
            nameTextFieldHOME.setBorder(BorderFactory.createLineBorder(Color.RED));
        }
        else {
            nameTextFieldHOME.setBorder(UIManager.getBorder("TextField.border"));
        }
        ////
        if (password.isEmpty()) {
            hasErrors = true;
            passfieldHOME.setBorder(BorderFactory.createLineBorder(Color.RED));
        }
        else {
            passfieldHOME.setBorder(UIManager.getBorder("TextField.border"));
        }
        
        if (program.isEmpty()) {
            hasErrors = true;
            programTextFieldHOME.setBorder(BorderFactory.createLineBorder(Color.RED));
        }
        else {
            programTextFieldHOME.setBorder(UIManager.getBorder("TextField.border"));
        }
        
      
        
        if (hasErrors) {
            JOptionPane.showMessageDialog(null, "The form you submitted is incomplete.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                InputStream file = getClass().getClassLoader().getResourceAsStream("BookInfo.xlsx");
                XSSFWorkbook workbook = new XSSFWorkbook(file);
                XSSFSheet sheet = workbook.getSheetAt(2);
                int lastRowNum = sheet.getLastRowNum();
                int lastID = 0;
                
                if (lastRowNum >= 1) {
                    XSSFCell lastIDCell = sheet.getRow(lastRowNum).getCell(0);
                    if (lastIDCell != null) {
                        lastID = (int) lastIDCell.getNumericCellValue();
                    }
                }
                
                Row row = sheet.createRow(lastRowNum + 1);
                int ID = lastID + 1;
                row.createCell(0).setCellValue(ID);
                
                row.createCell(1).setCellValue(name);
                row.createCell(3).setCellValue(studnum);
                row.createCell(5).setCellValue(yearlevel);
                row.createCell(7).setCellValue(program);
                row.createCell(9).setCellValue(password);
                
                
                lastID = ID;
                file.close();
                
                FileOutputStream outFile = new FileOutputStream(new File(getClass().getClassLoader().getResource("BookInfo.xlsx").getFile()));
                workbook.write(outFile);
                outFile.close();
                
                JOptionPane.showMessageDialog(null, "Account information saved!", "Success", JOptionPane.INFORMATION_MESSAGE);
                nameTextFieldHOME.setText("");
                studNumTextFieldHOME.setText("");
                programTextFieldHOME.setText("");
                passfieldHOME.setText("");
            }
            catch (IOException ex) {
                System.out.println("Error: " + ex.getMessage());
        }    
} 
    }//GEN-LAST:event_saveButtonHOMEActionPerformed

